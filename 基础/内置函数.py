# !/usr/bin/env python

print("返回数字的绝对值abs,", abs(-10))

# all() 函数用于判断给定的可迭代参数 iterable 中的所有元素是否不为 0、''、False 或者 iterable 为空，如果是返回 True，否则返回 False
print("all()函数用于判断给定的可迭代参数 iterable 中的所有元素是否不为 0、''、False 或者 iterable 为空，如果是返回 True，否则返回 False,", all(["", 1]))

# any() 函数用于判断给定的可迭代参数 iterable 是否全部为空对象，如果都为空、0、false，则返回 False，如果不都为空、0、false，则返回 True。
print("any() 函数用于判断给定的可迭代参数 iterable 是否全部为空对象，如果都为空、0、false，则返回 False，如果不都为空、0、false，则返回 True,", any(["", "", 1]))

# bin() 返回一个整数 int 或者长整数 long int 的二进制表示。
print("bin() 返回一个整数 int 或者长整数 long int 的二进制表示,", bin(3))

# bool() 函数用于将给定参数转换为布尔类型，如果没有参数，返回 False。
print("bool() 函数用于将给定参数转换为布尔类型，如果没有参数，返回 False", bool(""))

